import Foundation
import UIKit

extension UIColor {
    class func spotterRed() -> UIColor{
        return UIColor(red:0.862, green:0.275, blue:0.275, alpha:1.00)
    }
    class func spotterLightBlue() -> UIColor {
        return UIColor(red:0.55, green:0.78, blue:0.84, alpha:1.00)
    }
    class func spotterGray() -> UIColor {
        return UIColor(red:0.71, green:0.72, blue:0.73, alpha:1.00)
    }
    class func spotterLightGray() -> UIColor {
        return UIColor(red:0.90, green:0.91, blue:0.91, alpha:1.00)
    }
    class func spotterDarkGray() -> UIColor {
        return UIColor(red:0.50, green:0.51, blue:0.52, alpha:1.00)
    }
    class func spotterOffWhite() -> UIColor {
        return UIColor(red:0.95, green:0.95, blue:0.96, alpha:1.00)
    }
    
    class func facebookBlue() -> UIColor {
        return UIColor(red:0.231, green:0.349, blue:0.596, alpha:1.00)
    }
    
    class func textFieldBorderColor() -> UIColor {
        return UIColor(red: 0.8, green: 0.8, blue: 0.8, alpha: 1.00)
    }
}